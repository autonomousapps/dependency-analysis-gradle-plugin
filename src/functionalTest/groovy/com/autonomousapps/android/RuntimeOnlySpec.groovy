package com.autonomousapps.android

import com.autonomousapps.android.projects.TransitiveRuntimeProject
import com.autonomousapps.android.projects.TransitiveVariantRuntimeProject
import org.gradle.util.GradleVersion

import static com.autonomousapps.advice.truth.BuildHealthSubject.buildHealth
import static com.autonomousapps.utils.Runner.build
import static com.google.common.truth.Truth.assertAbout

class RuntimeOnlySpec extends AbstractAndroidSpec {

  def "transitive dependencies with runtime capabilities are added directly (#gradleVersion AGP #agpVersion)"() {
    given:
    def project = new TransitiveRuntimeProject(agpVersion as String)
    gradleProject = project.gradleProject

    when:
    build(gradleVersion as GradleVersion, gradleProject.rootDir, 'buildHealth')

    then:
    assertAbout(buildHealth())
      .that(project.actualBuildHealth())
      .isEquivalentIgnoringModuleAdviceAndWarnings(project.expectedBuildHealth)

    where:
    [gradleVersion, agpVersion] << gradleAgpMatrix()
  }

  def "transitive dependencies on variants with runtime capabilities are added directly (#gradleVersion AGP #agpVersion)"() {
    given:
    def project = new TransitiveVariantRuntimeProject(agpVersion as String)
    gradleProject = project.gradleProject

    when:
    build(gradleVersion as GradleVersion, gradleProject.rootDir, 'buildHealth')

    then:
    assertAbout(buildHealth())
      .that(project.actualBuildHealth())
      .isEquivalentIgnoringModuleAdviceAndWarnings(project.expectedBuildHealth)

    where:
    [gradleVersion, agpVersion] << gradleAgpMatrix()
  }
}
